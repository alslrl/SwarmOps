/**
 * Apply ±10% multiplicative variation to a pass_threshold value.
 * Clamped to [0, 1], rounded to 3 decimal places.
 *
 * @param {number} base - Archetype base pass_threshold (0–1)
 * @param {() => number} rng - RNG function returning a value in [0, 1)
 * @returns {number} Varied pass_threshold in [0, 1]
 */
function varyPassThreshold(base, rng) {
  const factor = rng() * 0.2 - 0.1; // uniform [-0.10, +0.10)
  const varied = base * (1 + factor);
  return Math.min(1, Math.max(0, Math.round(varied * 1000) / 1000));
}

/**
 * Spawn the full 800-agent buyer cohort from the canonical ARCHETYPES.
 *
 * This is the Gen 2 entry point for individual-agent simulation. Each agent:
 *   - Has a unique agent_id in the format "{archetype_id}_{NNNN}" (e.g. "price_sensitive_0001")
 *   - Has a unique Korean name generated by createKoreanNameGenerator (agentUtils.mjs)
 *   - Has independent ±10% multiplicative variation on every sensitivity field
 *     (price_sensitivity, trust_sensitivity, promo_affinity, brand_bias, pass_threshold),
 *     matching ontology buyer_agent.sensitivity_profile — NOT Gaussian noise
 *   - Is guaranteed to have a unique sensitivity profile (5-tuple) via rejection sampling
 *
 * Count distribution uses the largest-remainder method so that the sum of per-archetype
 * counts equals totalBuyers exactly.
 *
 * @param {object} [options]
 * @param {number} [options.totalBuyers=800] - Total number of agents to spawn
 * @param {number} [options.seed=42] - RNG seed for deterministic generation
 * @returns {Array<object>} Array of agent objects, length === totalBuyers
 * @throws {Error} If totalBuyers is not a positive integer or seed is not an integer
 */
export function spawnBuyerAgents({ totalBuyers = 800, seed = 42 } = {}) {
  if (!Number.isInteger(totalBuyers) || totalBuyers <= 0) {
    throw new Error(`[agent-spawner] totalBuyers must be a positive integer, got ${totalBuyers}`);
  }
  if (!Number.isInteger(seed)) {
    throw new Error(`[agent-spawner] seed must be an integer, got ${seed}`);
  }

  // Compute proportional per-archetype counts using largest-remainder method
  const counts = buildCohortCounts(ARCHETYPES, totalBuyers);

  // Name generator: Fisher-Yates shuffled pool of 1200 unique Korean names
  // (30 surnames × 40 given names). Separate seed derivation from trait RNG.
  const nameGen = createKoreanNameGenerator(seed);

  // Trait RNG: derive a well-separated seed so the trait stream is uncorrelated
  // with the name generator's internal stream even when both start from the same seed.
  const traitSeed = Math.imul(seed ^ (seed >>> 16), 0x45d9f3b) >>> 0;
  const rng = mulberry32(traitSeed);

  // Rejection sampling: track every 5-tuple profile seen so far.
  // sensitivity_variation() draws uniformly from [-10%, +10%), yielding discrete
  // values at 0.1 resolution across [1, 5]. With 4 sensitivity fields + pass_threshold
  // there are thousands of unique combinations per archetype, far exceeding the
  // largest cohort (144 agents). The 500-attempt guard prevents infinite loops.
  const profilesSeen = new Set();

  const agents = [];
  let globalIndex = 0;

  for (const { archetype, count } of counts) {
    for (let i = 0; i < count; i++) {
      globalIndex += 1;
      const paddedIndex = String(globalIndex).padStart(4, '0');
      const agent_id = `${archetype.id}_${paddedIndex}`;
      const korean_name = nameGen.next();

      // Rejection-sample until we get a unique sensitivity profile
      let traits;
      let profile;
      let attempts = 0;
      do {
        traits = {
          price_sensitivity: sensitivity_variation(archetype.price_sensitivity, rng),
          trust_sensitivity: sensitivity_variation(archetype.trust_sensitivity, rng),
          promo_affinity: sensitivity_variation(archetype.promo_affinity, rng),
          brand_bias: sensitivity_variation(archetype.brand_bias, rng),
          pass_threshold: varyPassThreshold(archetype.pass_threshold, rng),
        };
        profile = `${traits.price_sensitivity}|${traits.trust_sensitivity}|${traits.promo_affinity}|${traits.brand_bias}|${traits.pass_threshold}`;
        attempts += 1;
        if (attempts > 500) {
          throw new Error(
            `[agent-spawner] Cannot generate unique sensitivity profile for archetype `
            + `"${archetype.id}" after 500 attempts — profile space may be exhausted.`,
          );
        }
      } while (profilesSeen.has(profile));

      profilesSeen.add(profile);

      agents.push({
        agent_id,
        korean_name,
        archetype_id: archetype.id,
        budget_band: archetype.budget_band,
        price_sensitivity: traits.price_sensitivity,
        trust_sensitivity: traits.trust_sensitivity,
        promo_affinity: traits.promo_affinity,
        brand_bias: traits.brand_bias,
        pass_threshold: traits.pass_threshold,
        copy_preference: archetype.copy_preference,
      });
    }
  }

  // ── Post-spawn invariants ────────────────────────────────────────────────

  if (agents.length !== totalBuyers) {
    throw new Error(
      `[agent-spawner] Expected ${totalBuyers} agents but spawned ${agents.length}`,
    );
  }

  if (profilesSeen.size !== totalBuyers) {
    throw new Error(
      `[agent-spawner] Expected ${totalBuyers} unique sensitivity profiles but got ${profilesSeen.size}`,
    );
  }

  return agents;
}
