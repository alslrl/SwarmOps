/**
 * agent-spawner.mjs
 *
 * Defines the canonical 8-archetype data for the individual-agent simulation pipeline.
 *
 * The ARCHETYPES constant is the single source of truth for archetype definitions
 * in Gen 2 code. Each archetype object matches fixtures/buyer-personas.md exactly.
 *
 * Archetype field contract:
 *   - id / archetype_id  : snake_case identifier string (both fields present for compat)
 *   - label              : Korean display name
 *   - cohort_weight_percent : integer, all 8 must sum to 100
 *   - budget_band        : "low" | "mid" | "high"
 *   - price_sensitivity  : base integer [1–5]
 *   - trust_sensitivity  : base integer [1–5]
 *   - promo_affinity     : base integer [1–5]
 *   - brand_bias         : base integer [1–5]
 *   - pass_threshold     : base float [0–1], probability to skip all purchases
 *   - copy_preference    : Korean phrase describing what copy resonates with this archetype
 *
 * Per-agent trait values are the archetype base ± small Gaussian noise applied at
 * spawn time (see spawnAgentCohort in buyerAgent.mjs). Mock-mode noise uses ±10%
 * multiplicative jitter on the base value, per ontology buyer_agent.sensitivity_profile.
 *
 * Gen 2 entry point: spawnBuyerAgents() generates the full 800-agent cohort using
 * the ARCHETYPES constant defined below, with ±10% multiplicative variation per
 * ontology buyer_agent.sensitivity_profile (sensitivity_variation from agentUtils.mjs).
 */

